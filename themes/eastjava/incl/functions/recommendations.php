		<h3>Updates and Backing Up</h3>

		<p>All One Page Love themes are continuously updated and improved. Each time a new update is released, 
		you will receive an update notification in your WordPress Dashboard. It is important to know is if you update
		the theme using the update function, all your custom code edits are overwritten. So you will lose custom code edits.</p>

		<p>To ensure your updates go smoothly, we recommend these 2 plugins:</p>

		<p>
		  Tip 1: For statistics, use the very lightweight plugin called <a href="http://wordpress.org/extend/plugins/googleanalytics/" target="_blank">Google Analytics</a><br />
		Tip 2: For custom CSS edits, use this neat plugin called <a href="http://wordpress.org/plugins/simple-custom-css/" target="_blank">Simple Custom CSS</a><br /><br />	
		<em>The above two plugins store stats and style info outside of the theme folder, so upgrading your theme does not effect the relevant code.</em>  
		</p>
  
